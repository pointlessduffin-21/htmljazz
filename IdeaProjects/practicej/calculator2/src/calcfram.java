    private void buttonequalsActionPerformed(java.awt.event.ActionEvent evt) {
        String answer;

        secondnum = Double.parseDouble(screen.getText());
        if (operations == "+")
        {
            result = (firstnum) + (secondnum);
            answer = String.format("%.0f",result);
            screen.setText(answer);
        }
        else if (operations == "-")
        {
            result = (firstnum) - (secondnum);
            answer = String.format("%.0f",result);
            screen.setText(answer);
        }
        else if (operations == "*")
        {
            result = (firstnum) * (secondnum);
            answer = String.format("%.0f",result);
            screen.setText(answer);
        }
        else if (operations == "/")
        {
            result = (firstnum) / (secondnum);
            answer = String.format("%.0f",result);
            screen.setText(answer);
        }
        else if (operations == "%")
        {
            result = (firstnum) % (secondnum);
            answer = String.format("%.0f",result);
            screen.setText(answer);
        }
    }

