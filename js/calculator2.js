function addition(num1, num2){
    result = num1+num2;
    return result;
}

function subtraction(num1, num2){
    result = num1-num2;
    return result;
}

function multiplication(num1, num2){
    result = num1*num2;
    return result;
}

function division(num1, num2){
    result = num1/num2;
    return result;
}

console.log(addition(5,4));
console.log(subtraction(6,4));
console.log(multiplication(7,4));
console.log(division(8,4));